#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << pow(2, 5) << endl; // 2^5
    cout << sqrt(36) << endl; // square route of 36
    cout << round(4.3) << endl; // Rounds 4.3
    cout << ceil(4.5) << endl; // round up 4.5
    cout << floor(4.5) << endl; // round down 4.5
    cout << fmax(3, 10) << endl; // returns the bigger number
    cout << fmin(3, 10) << endl; // returns the smallest number
}